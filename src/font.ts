export interface TextOpts {
  /** Integer pixel scale (1 = native 5x7). */
  scale?: number;
  /** CSS color string. */
  color?: string;
}

/** Width in native px of `text` at the given scale (includes inter-glyph spacing). */
export function textWidth(text: string, scale = 1): number {
  return text.length * CELL_W * scale;
}

/**
 * Draw bitmap text at (x, y) — y is the glyph top. Pixels are hard (fillRect),
 * so they upscale crisply with the rest of the buffer.
 */
export function drawText(ctx: CanvasRenderingContext2D, text: string, x: number, y: number, opts: TextOpts = {}): void {
  const { scale = 1, color = "#ffffff" } = opts;
  ctx.fillStyle = color;
  let cx = x | 0;
  const top = y | 0;
  for (let i = 0; i < text.length; i++) {
    const ch = text.charCodeAt(i) & 0xff;
    const base = ch * GLYPH_COLS;
    for (let col = 0; col < GLYPH_COLS; col++) {
      const bits = FONT_5X7[base + col];
      for (let row = 0; row < GLYPH_ROWS; row++) {
        if (bits & (1 << row)) ctx.fillRect(cx + col * scale, top + row * scale, scale, scale);
      }
    }
    cx += CELL_W * scale;
  }
}
